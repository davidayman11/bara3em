            Text(
              'Sign in to continue!',
              style: TextStyle(
                fontSize: 18,
                color: Colors.blueAccent.withOpacity(1),
              ),
            ),
            SizedBox(height: screenHeight * 0.06),
            TextFormField(
              controller: emailController,
