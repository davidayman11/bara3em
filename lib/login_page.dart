class SimpleLoginScreen extends StatefulWidget {
  @override
  _SimpleLoginScreenState createState() => _SimpleLoginScreenState();
}

class _SimpleLoginScreenState extends State<SimpleLoginScreen> {
  final TextEditingController emailController = TextEditingController();
  final TextEditingController passwordController = TextEditingController();

  String? emailError;
  String? passwordError;

  bool validate() {
    setState(() {
      emailError = null;
      passwordError = null;
    });

    final emailExp = RegExp(
      r"^[a-zA-Z0-9.!#$%&'*+/=?^_`{|}~-]+@[a-zA-Z0-9](?:[a-zA-Z0-9-]{0,253}[a-zA-Z0-9])?(?:\.[a-zA-Z0-9](?:[a-zA-Z0-9-]{0,253}[a-zA-Z0-9])?)*$",
    );

    if (emailController.text.isEmpty || !emailExp.hasMatch(emailController.text)) {
      setState(() {
        emailError = 'Email is invalid';
      });
      return false;
    }

    if (passwordController.text.isEmpty) {
      setState(() {
        passwordError = 'Please enter a password';
      });
      return false;
    }

    return true;
  }

  void submit() async {
    if (validate()) {
      try {
        final userCredential = await FirebaseAuth.instance.signInWithEmailAndPassword(
          email: emailController.text,
          password: passwordController.text,
        );
        // Navigate to Bara3emHomePage after successful login
        Navigator.pushReplacement(
          context,
          MaterialPageRoute(builder: (_) => Bara3emHomePage()),
        );
      } on FirebaseAuthException catch (e) {
        print("Firebase Auth Exception: ${e.message}");
        if (e.code == 'user-not-found' || e.code == 'wrong-password') {
          // Show error message in a dialog
          showErrorMessage('Invalid email or password');
        } else {
          // Handle other errors, maybe set some error message to show to the user
          print("Other Firebase Auth Exception");
          print(e.message); // Consider using something like `setState` to show an error message
        }
      } catch (e, stackTrace) {
        print("General Exception: $e");
        print(stackTrace);
      }
    }
  }

  void showErrorMessage(String message) {
    showDialog(
      context: context,
      builder: (BuildContext context) {
        return AlertDialog(
          title: Text("Error"),
          content: Text(message),
          actions: <Widget>[
            TextButton(
              onPressed: () {
                Navigator.of(context).pop();
              },
              child: Text("OK"),
            ),
          ],
        );
      },
    );
  }

  @override
  Widget build(BuildContext context) {
    final screenHeight = MediaQuery.of(context).size.height;

    return Scaffold(
